package HubbmBazaar;

public class Person {
	protected String name, email, dateOfBirth;
	/**
	 * This method is the constructor of Person class. It is the superclass
	 * of Employee and Customer.
	 * @param name
	 * @param email
	 * @param dateOfBirth
	 */
	public Person(String name, String email, String dateOfBirth) {
		super();
		this.name = name;
		this.email = email;
		this.dateOfBirth = dateOfBirth;
	}

	
		
	
}
